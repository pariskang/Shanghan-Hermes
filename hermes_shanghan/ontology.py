_TERM_TO_GROUPS: Dict[str, List[int]] = {}
for gi, group in enumerate(SYNONYM_GROUPS):
    for term in group:
        _TERM_TO_GROUPS.setdefault(normalize_query(term), []).append(gi)


_ALL_TERMS = sorted({normalize_query(w) for g in SYNONYM_GROUPS for w in g},
                    key=lambda t: (-len(t), t))


def detect_terms(text: str, max_terms: int = 4) -> List[str]:
    """在自由文本中檢出本體組詞（最長優先，去重）——
    「水腫的證治」→ [水腫]，供擴展通道使用。"""
    q = normalize_query(text)
    out: List[str] = []
    for t in _ALL_TERMS:
        if t in q and not any(t in o for o in out):
            out.append(t)
            if len(out) >= max_terms:
                break
    return out


def expand_term(term: str, max_terms: int = 8) -> List[str]:
    """同義組擴展：返回包含輸入詞的所有組的並集（輸入詞排首位）。"""
    t = normalize_query(term.strip())
    out: List[str] = [t] if t else []
    for gi in _TERM_TO_GROUPS.get(t, []):
        for w in SYNONYM_GROUPS[gi]:
            wn = normalize_query(w)
            if wn not in out:
                out.append(wn)
    # 子串命中（如輸入「骨節疼痛」含組詞「骨節疼痛」已中；輸入「腰背痛」
    # 未收錄則嘗試其尾詞「痛」不擴——僅整詞命中，避免噪聲）
    return out[:max_terms]


def expand_terms(terms: List[str], max_total: int = 16) -> List[str]:
    out: List[str] = []
    for t in terms:
        for w in expand_term(t):
            if w not in out:
                out.append(w)
    return out[:max_total]
